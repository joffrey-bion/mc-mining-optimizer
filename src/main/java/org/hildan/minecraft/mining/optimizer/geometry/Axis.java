package org.hildan.minecraft.mining.optimizer.geometry;

/**
 * Defines a spatial dimensions.
 */
public enum Axis {
    /**
     * The X axis is horizontal, pointing East.
     */
    X,
    /**
     * The Y axis is vertical, pointing up.
     */
    Y,
    /**
     * The Z axis is horizontal, pointing South.
     */
    Z
}
